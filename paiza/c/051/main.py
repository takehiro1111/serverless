"""C051:カード並べ

フライト中に退屈してしまったあなたは、持っているトランプを使用した簡単な遊びを考えました。

この遊びでは 1 から 9 までの数字のカードのみを 4 枚使用します。
ただし、この 4 枚の中で同じ数値のカードが重複する事もあります。
ルールは以下の通りです。

4 枚のカードを横に並べます。
左から 2 枚のカード、右から 2 枚のカードをそれぞれ 2 ケタの整数とみなし、和を計算します。
あらゆる並べ方を試し、和の最大値（最大スコア）を求めます。

カード 4 枚に書かれた数が与えられるので、最大スコアを出力してください。
入力例 1 の 2, 9, 3, 8 の 4 枚を使う場合、以下の 12 通りの和の最大値 175 が最大スコアとなります。

カードの 4 枚の並べ方は 4! = 24 通り存在しますが、足し算は順序に依存しないため、12 通りのみ考慮すればよいことに注意してください。
たとえば、9, 2, 3, 8 の順で並べた場合のスコアは 92 + 38 = 130 となります。

・92 + 38 = 130
・92 + 83 = 175
・93 + 28 = 121
・93 + 82 = 175
・98 + 23 = 121
・98 + 32 = 130
・39 + 28 = 67
・38 + 29 = 67
・82 + 39 = 121
・89 + 23 = 112
・89 + 32 = 121
・83 + 29 = 112
"""

# 4枚のカードの入力を受け取る
cards = list(map(int, input().split()))
# [2,9,3,8]

# 最大スコアを保持する変数
max_score = 0

# すべての並べ方を試す（4枚のカードのすべての組み合わせ）
for i in range(4):
    for j in range(4):
        if j == i:  # 同じカードは使えない
            continue
        for k in range(4):
            if k == i or k == j:  # 既に使用したカードは使えない
                continue
            for l in range(4):
                if l == i or l == j or l == k:  # 既に使用したカードは使えない
                    continue

                # 2桁の数を2つ作る
                num1 = cards[i] * 10 + cards[j]  # 左から2枚で1つ目の2桁の数
                num2 = cards[k] * 10 + cards[l]  # 右から2枚で2つ目の2桁の数

                # スコアを計算して最大値を更新
                score = num1 + num2
                max_score = max(max_score, score)

# 最大スコアを出力
print(max_score)

